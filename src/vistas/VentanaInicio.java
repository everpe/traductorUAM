    private void lblAgregarTerminoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAgregarTerminoMouseClicked
       this.click();
       this.crearInteractividadBotones(lblAgregarTermino, true);
       AgregarTermino agregar = new AgregarTermino(this, true, cliente);//, "Término");
       agregar.setVisible(true);
       Termino t1= agregar.getT1();
       Termino t2=agregar.getT2();
       if(t1 != null && t2 != null)
       {
           String terminos = t1.getPalabra().getId() + "/" + t1.getPalabra().getTermino()
                   + "/" + t1.getIdioma().getIso() + "/" + t1.getIdioma().getNombre() + ":"
                   + t2.getPalabra().getId() + "/" + t2.getPalabra().getTermino()
                   + "/" + t2.getIdioma().getIso() + "/" + t2.getIdioma().getNombre();
           String respuesta = this.cliente.hablar("agregar,traduccion," + terminos);
           
            if(respuesta.equals("si"))
                JOptionPane.showMessageDialog(this, "Se Agregó Correctamente");
            else
                JOptionPane.showMessageDialog(this, "No se pudó agregar");
            
       }   
    }//GEN-LAST:event_lblAgregarTerminoMouseClicked

