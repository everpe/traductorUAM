    private void btnTraducirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraducirActionPerformed
        String[] idiOrigen = ((String)this.comboIdiomaOrigen.getSelectedItem()).split(":");
        String[] idiDest = ((String)this.comboIdiomaDestino.getSelectedItem()).split(":");

        String[] palabras = (this.txtFrase.getText().toLowerCase()).split(" ");
        String palabraSeparada = "";
        
        for (int i = 0; i < palabras.length; i++) 
        {
            palabraSeparada += palabras[i] + "-";            
        }
        
        //le envio el idioma origen y destino.
        String isos = idiOrigen[0] + "!" + idiDest[0];
        
        
        String rpta = this.cliente.hablar("traducirFrase," + isos + "," + palabraSeparada);
        this.txtTraduccion.setText(rpta.replace("-", " "));
            
    }//GEN-LAST:event_btnTraducirActionPerformed

